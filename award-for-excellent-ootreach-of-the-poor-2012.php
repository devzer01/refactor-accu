<h1>Credit Union Microfinance Innovation (CUMI) Award for Excellent Outreach of the Poor -  2011</h1>
<div class="content-body content-body-nobg">
<h3>Rationale:</h3>
  <p>The Asian credit unions represent 35 million individual members from 12,827 credit unions in 20 countries in Asia. Elite dominated cooperative financial institutions remain the public perception of credit unions proven by the credit union outreach of the ‘have less.’ Only 12% of credit union members belong to the poor. Though the number may be significant as the credit unions are able to reach over 5 million poor client-members, a huge number of poor people are still not reached by credit u unions in many developing countries. Without exerting additional efforts and implementing focused program for the poor, credit unions were able to reach the poor.  However, alleviating these members from poverty may not be visible in the absence of an integrated approach to ensure the microfinance services create impact in improving the lives of the poor. CUMI is an integrated approach wherein the program includes education, developing habit of thrift, access to credit and support to entrepreneurship.</p>
<p>
ACCU recognizes that a wide ranging CUMI program should be implemented nationally to provide sustainable impact to the poor people. Majority of the 5.8 million poor who have gained access to credit union financial services have been recruited based on the traditional approach of credit unions. </p>

<h3>CUMI is introducing innovations such as:</h3>
<ul class="sec">
<li>Focused on building the long term sustainability of the poor by developing the habit of thrift and financial discipline</li>
<li>Credit (loan) is not a right but a privilege. Loan will be granted to finance small business to generate new income or expand existing business not for consumption purpose.</li>
<li>Flexibility on the membership requirement such as the Share Capital contribution. In the traditional approach, members have to contribute at least US$ 50 for membership. In CUMI, the share can be paid in small instalments.</li>
<li>Education – on family financial planning, savings habit, business management etc.</li>
<li>Business Development Services provided by the credit unions and its federations</li>
</ul>

<p>CUMI is now active in 6 countries in Asia but outreach and growth rate are still low. Till end of last year only 295,707 people were reached by 553 credit unions out of 12,827 credit unions in 6 developing countries in Asia.  ACCU has launched the program with it members to encourage credit unions to reach the at least 1,000,000 poor served by CUMI from at least 1,000 credit unions in 6 counties in 2012</p>

<p>ACCU institutes a regular and permanent means of recognizing the Credit Union Microfinance Innovation (CUMI) performance to recognize significant outreach with quality and excellent services to the low income members in the community.</p>
<p>The regional Award establishes that, on the occasion of the Asian Credit Union Forum plaques of appreciation would be presented publicly and officially on behalf of the Asia-wide credit union movement.</p>
<p>Meanwhile, the national Award establishes that, on the occasion of the National Credit Union Congress or Annual General Meeting of ACCU member organization, plaques of appreciation would be presented publicly and officially on behalf of the nation-wide credit union movement.</p>

